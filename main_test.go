func TestApplyCommentTextFallback(t *testing.T) {
	oldDryRun := dryRun
	defer func() { dryRun = oldDryRun }()

	dryRun = true

	tmpDir := t.TempDir()
	testFile := filepath.Join(tmpDir, "test.go")

	// This code won't be found by AST lookup - will trigger fallback
	src := `package main

func main() {
	a, b := foo()
	_ = b
}
func foo() (int, int) { return 1, 2 }
`
	if err := os.WriteFile(testFile, []byte(src), 0o600); err != nil {
		t.Fatalf("Failed to write test file: %v", err)
	}

	fset := token.NewFileSet()
	f, err := parser.ParseFile(fset, testFile, nil, parser.ParseComments)
	if err != nil {
		t.Fatalf("Failed to parse file: %v", err)
	}

	// Target a line that doesn't exist in AST - will trigger fallback
	target := Target{File: testFile, Line: 999, Identifier: "nonexistent", Kind: TargetDecl}

	modified := applyCommentText(testFile, fset, f, target)
	// Should return false since nothing was modified
	if modified {
		t.Error("Expected modified to be false for nonexistent target")
	}
}

func TestApplyCommentTextFallbackWithDryRun(t *testing.T) {
	oldDryRun := dryRun
	defer func() { dryRun = oldDryRun }()

	dryRun = true

	tmpDir := t.TempDir()
	testFile := filepath.Join(tmpDir, "test.go")

	// Short assignment - will trigger fallback in normal path too
	src := `package main

func main() {
	a, b := foo()
	_ = b
}
func foo() (int, int) { return 1, 2 }
`
	if err := os.WriteFile(testFile, []byte(src), 0o600); err != nil {
		t.Fatalf("Failed to write test file: %v", err)
	}

	fset := token.NewFileSet()
	f, err := parser.ParseFile(fset, testFile, nil, parser.ParseComments)
	if err != nil {
		t.Fatalf("Failed to parse file: %v", err)
	}

	// Target 'a' in short assignment - will fallback to rewrite
	target := Target{File: testFile, Line: 4, Identifier: "a", Kind: TargetDecl}

	modified := applyCommentText(testFile, fset, f, target)
	// In dryRun mode with fallback, should return modified=true but not write
	if !modified {
		t.Error("Expected modified to be true for short assignment fallback")
	}

	// Verify file was NOT modified (dryRun)
	content, _ := os.ReadFile(testFile)
	if !strings.Contains(string(content), "a, b :=") {
		t.Error("File should not have been modified in dryRun mode")
	}
}

