func TestIsGenerated(t *testing.T) {
	tests := []struct {
		name     string
		content  string
		expected bool
	}{
		{
			name:     "generated file",
			content:  "// Code generated by something DO NOT EDIT\npackage main",
			expected: true,
		},
		{
			name:     "regular file",
			content:  "package main\n\nfunc main() {}",
			expected: false,
		},
		{
			name:     "non-generated with code generated text",
			content:  "package main\n// This code was generated for testing",
			expected: false,
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			tmpDir := t.TempDir()
			testFile := filepath.Join(tmpDir, "test.go")
			if err := os.WriteFile(testFile, []byte(tt.content), 0o600); err != nil {
				t.Fatalf("Failed to write test file: %v", err)
			}
			got := isGenerated(testFile)
			if got != tt.expected {
				t.Errorf("isGenerated(%q) = %v, want %v", tt.name, got, tt.expected)
			}
		})
	}
}

